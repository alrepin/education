package ga.repin.education.course02.topic14.hw.StringList;

public class StringListIllegalArgumentException extends IllegalArgumentException {
    public StringListIllegalArgumentException() {
        super();
    }
    public StringListIllegalArgumentException(String s) {
        super(s);
    }

    public StringListIllegalArgumentException(String message, Throwable cause) {
        super(message, cause);
    }

    public StringListIllegalArgumentException(Throwable cause) {
        super(cause);
    }

}
